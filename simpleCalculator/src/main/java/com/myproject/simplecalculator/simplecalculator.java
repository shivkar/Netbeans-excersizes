package com.myproject.simplecalculator;

/**
 *
 * @author SHIVALI
 */
public class simplecalculator {

    /**
     *
     * @param a
     * @param b
     * @return
     */
    public int Add(int a, int b) {

        return (a + b);
    }

    public int Substract(int a, int b) {
        return (a - b);
    }

    public int Multiply(int a, int b) {
        return (a * b);
    }

    public int Divide(int a, int b) {
        return (a / b);
    }
}

